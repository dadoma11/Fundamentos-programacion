// Definimos una clase llamada Test
class Test {
    
    // Método principal 'main' que es el punto de entrada cuando ejecutamos el programa
    public static void main(String[] args) {

        // Declaramos una variable entera 'a' y la inicializamos en 0
        int a;

        // Instrucción condicional if, que siempre será verdadera porque se le da el valor 'true'
        if (true) {
            // Si la condición es verdadera (que siempre lo es en este caso),
            // la variable 'a' se asigna el valor de 5
            a = 5;
        }

        // Imprimimos el valor actual de la variable 'a' en la consola
        // En este caso, siempre será 5 porque la condición if siempre se cumple
        System.out.println(a);
    }
}
