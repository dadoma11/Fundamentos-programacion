public class Ejercicio7 {
    public static void main(String[] args) {
        int suma=30;
        System.out.println("Operaciones con sumas: " + suma++ + "  " + ++suma + "   " + suma + "   " + suma--);
        System.out.println("Suma: "+ suma);
    }
}
// Salida: 
// Operaciones con sumas: 30  32   32   32
// Suma: 31